def build_docx(body_xml_parts: List[str], output_path: str) -> None:
    """
    Assemble a .docx file from a list of Open XML body element strings.

    Args:
        body_xml_parts: List of XML strings representing body elements
            (paragraphs, tables, etc.) in document order.
        output_path: Filesystem path to write the resulting .docx file.
    """
    body = "\n".join(body_xml_parts)
    document_xml = (
        '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>\n'
        '<w:document xmlns:w="http://schemas.openxmlformats.org/wordprocessingml/2006/main"\n'
        '            xmlns:r="http://schemas.openxmlformats.org/officeDocument/2006/relationships">\n'
        f'  <w:body>\n{body}\n'
        '    <w:sectPr>\n'
        '      <w:pgSz w:w="12240" w:h="15840"/>\n'
        '      <w:pgMar w:top="1440" w:right="1440" w:bottom="1440" w:left="1440"\n'
        '               w:header="720" w:footer="720" w:gutter="0"/>\n'
        '    </w:sectPr>\n'
        '  </w:body>\n'
        '</w:document>'
    )

    buf = io.BytesIO()
    with zipfile.ZipFile(buf, "w", zipfile.ZIP_DEFLATED) as zf:
        zf.writestr("[Content_Types].xml", _CONTENT_TYPES)
        zf.writestr("_rels/.rels", _RELS)
        zf.writestr("word/document.xml", document_xml)
        zf.writestr("word/_rels/document.xml.rels", _DOCUMENT_RELS)
        zf.writestr("word/styles.xml", _STYLES)
        zf.writestr("word/numbering.xml", _NUMBERING)

    with open(output_path, "wb") as f:
        f.write(buf.getvalue())
